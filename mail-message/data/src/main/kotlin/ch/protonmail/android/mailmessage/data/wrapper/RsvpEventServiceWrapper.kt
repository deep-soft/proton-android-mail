package ch.protonmail.android.mailmessage.data.wrapper

import arrow.core.Either
import arrow.core.left
import arrow.core.right
import ch.protonmail.android.mailcommon.data.mapper.toDataError
import ch.protonmail.android.mailcommon.domain.model.DataError
import uniffi.proton_mail_uniffi.RsvpAnswer
import uniffi.proton_mail_uniffi.RsvpEvent
import uniffi.proton_mail_uniffi.RsvpEventGetResult
import uniffi.proton_mail_uniffi.RsvpEventService
import uniffi.proton_mail_uniffi.VoidAnswerRsvpResult

class RsvpEventServiceWrapper(
    private val rsvpEventService: RsvpEventService
) {

    suspend fun answer(answer: RsvpAnswer): Either<DataError, Unit> =
        when (val result = rsvpEventService.answer(answer)) {
            is VoidAnswerRsvpResult.Error -> result.v1.toDataError().left()
            is VoidAnswerRsvpResult.Ok -> Unit.right()
        }

    fun get(): Either<DataError, RsvpEvent> = when (val result = rsvpEventService.get()) {
        is RsvpEventGetResult.Error -> result.v1.toDataError().left()
        is RsvpEventGetResult.Ok -> result.v1.right()
    }
}
