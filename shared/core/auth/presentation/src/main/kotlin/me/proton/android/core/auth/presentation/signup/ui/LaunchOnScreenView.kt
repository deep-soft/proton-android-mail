package me.proton.android.core.auth.presentation.signup.ui

import androidx.compose.runtime.Composable
import androidx.compose.runtime.LaunchedEffect
import androidx.compose.ui.platform.LocalSavedStateRegistryOwner
import androidx.lifecycle.compose.LocalLifecycleOwner
import me.proton.core.presentation.utils.launchOnScreenView
import uniffi.proton_mail_uniffi.SignupScreenId

@Composable
internal fun SignupScreenId.LaunchOnScreenView(onScreenView: (SignupScreenId) -> Unit) {
    val lifecycleOwner = LocalLifecycleOwner.current
    val savedStateRegistryOwner = LocalSavedStateRegistryOwner.current
    LaunchedEffect(this) {
        lifecycleOwner.launchOnScreenView(savedStateRegistryOwner.savedStateRegistry) {
            onScreenView(this@LaunchOnScreenView)
        }
    }
}
